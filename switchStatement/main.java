public class main {
    public static void main(String a[])
    {
        int n = 10;

        switch (n)
        {
            case 1:
                System.out.println("ONE");
                break;
            case 2:
                System.out.println("TWO");
                break;
            case 3:
                System.out.println("THREE");
                break;
            default:
                System.out.println("UNKNOWN");
        }
    }
}