public class DataTypes {
    public static void main(String a[]) 
    {
        // Integer Data Type
        int int1 = 12; // 4bytes
        short b = 21; // 2 bytes
        long c = 4342; // 8 bytes
        byte d = 127; // 1byte


        // Character Data Type
        char e = 'b'; // 2bytes. a character should be in single quotes

        //Float Data Type
        // by default point values are considered double
        double float1 = 50.892; //8 bytes 
        Float  float2 = 50.2f; //4bytes since point values are considered double an 'f' is appended on this to force float

        //Boolean Data type
        Boolean bool1 = true;
        Boolean bool2 = false;


    }
}